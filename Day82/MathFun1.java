import static java.lang.Math.floor;
import static java.lang.Math.abs; 
import  static java.lang.Math.*;   
public class MathFun1 {

    public static void main(String[] args) {  //in below without  class we are calling math function. import  the static  with math functions.
        
        System.out.println(min(2, 0));
        System.out.println(max(4, 0));
        System.out.println(sqrt(3));
        System.out.println(round(1.7));
        System.out.println(floor(8.9));
        System.out.println(ceil(8.6));
        System.out.println(abs(-9));
    }
    
}

